## This set of functions act to create the inverse of a matrix, and retrieve
##the inverse of already calculated, else calculates the inverse newly.

## This function returns the inverse of a matrix, which is cached in the
## first function makeCacheMatrix.

makeCacheMatrix <- function(x = numeric()) {
  m <- NULL
  set <- function(y) {
    x <<- y
    m <<- NULL
  }
  get <- function() x
  setinv <- function(solve) m <<- solve
  getinv <- function() m
  list(set = set, get = get,
       setinv = setinv,
       getinv = getinv)
}




